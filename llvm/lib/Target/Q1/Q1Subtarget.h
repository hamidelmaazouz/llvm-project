#ifndef LLVM_LIB_TARGET_Q1_Q1SUBTARGET_H
#define LLVM_LIB_TARGET_Q1_Q1SUBTARGET_H

#include "MCTargetDesc/Q1BaseInfo.h"
#include "Q1FrameLowering.h"
#include "Q1ISelLowering.h"
#include "Q1InstrInfo.h"
#include "Q1RegisterInfo.h"
#include "llvm/CodeGen/RegisterBankInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetFrameLowering.h"

#define GET_SUBTARGETINFO_HEADER
#include "Q1GenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class Q1Subtarget : public Q1GenSubtargetInfo {
  virtual void anchor();

#define GET_SUBTARGETINFO_MACRO(ATTRIBUTE, DEFAULT, GETTER)                    \
  bool ATTRIBUTE = DEFAULT;
#include "Q1GenSubtargetInfo.inc"

  Q1FrameLowering FrameLowering;
  Q1InstrInfo InstrInfo;
  Q1TargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  Q1Subtarget(const Triple &TT, StringRef CPU, StringRef FS,
              const TargetMachine &TM);

  // Parses features string setting specified subtarget options. The
  // definition of this function is auto-generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const TargetFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }

  const Q1InstrInfo *getInstrInfo() const override { return &InstrInfo; }

  const Q1RegisterInfo *getRegisterInfo() const override {
    return &InstrInfo.getRegisterInfo();
  }

  const Q1TargetLowering *getTargetLowering() const override { return &TLInfo; }

  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }

#define GET_SUBTARGETINFO_MACRO(ATTRIBUTE, DEFAULT, GETTER)                    \
  bool GETTER() const { return ATTRIBUTE; }
#include "Q1GenSubtargetInfo.inc"
};
} // end namespace llvm

#endif
